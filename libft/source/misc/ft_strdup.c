#include "../../include/libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s) + 1;
	copy = malloc(len);
	if (!copy)
		return (0);
	ft_strlcpy(copy, s, len);
	return (copy);
}
